package com.calculardias;

public class Ano {
    private int anoAtual = 2024;
    private int diasDoAno = 365;
    private int diasDoMes = 30;

    public int getAnoAtual() {
        return anoAtual;
    }

    public void setAnoAtual(int anoAtual) {
        this.anoAtual = anoAtual;
    }

    public int getDiasDoAno() {
        return diasDoAno;
    }

    public void setDiasDoAno(int diasDoAno) {
        this.diasDoAno = diasDoAno;
    }

    public int getDiasDoMes() {
        return diasDoMes;
    }

    public void setDiasDoMes(int diasDoMes) {
        this.diasDoMes = diasDoMes;
    }
}

